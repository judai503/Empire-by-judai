let isRunning = false

function start(file) {
  if (isRunning) return
  isRunning = true

  // üëâ EJECUTA EL start.js REAL DEL BOT (RA√çZ)
  let args = [join(__dirname, file), ...process.argv.slice(2)]

  setupMaster({ exec: args[0], args: args.slice(1) })
  let p = fork()

  p.on('exit', (_, code) => {
    isRunning = false
    if (code !== 0) start(file)
  })
}

